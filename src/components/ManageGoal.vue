<template>
  <div>
    <h2>Manage Goal</h2>
    <input type="text" ref="goal" />
    <button @click="setGoal">Add Goal</button>
    <teleport to="body">
      <error-alert v-if="inputValue">
        <h2>Input is invalid!</h2>
        <p>Pls enter at least few characters...</p>
        <button @click="goBack">Okay</button>
      </error-alert>
    </teleport>
  </div>
</template>

<script>
import ErrorAlert from './ErrorAlert.vue';

export default {
  components: {
    ErrorAlert,
  },
  data() {
    return {
      inputValue: false,
    };
  },
  methods: {
    setGoal() {
      const enteredValue = this.$refs.goal.value;
      if (enteredValue === "") {
        // alert("This Input should not be empty");
        this.inputValue = true;
      }
    },
    goBack() {
      this.inputValue = false;
    },
  },
};
</script>
